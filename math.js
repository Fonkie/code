//将a（0.461512）和b（0.571401）转化为百分比数值，小数点后保留两位
(function(){

var a = 0.461512;

console.log(a.toFixed(4)*100);//错误：46.150000000000006

console.log(Math.round(a*10000)/100);//正确：46.15

var b = 0.571401;
console.log((Math.round(b*100)/100)*100);//错误：56.99999999999999
console.log(Math.round(b*10000)/100);//正确：57.14

})();

//注意：在做任何乘除操作前，确定被操作数已被转成整数
